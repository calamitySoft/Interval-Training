//
//	Usage:
//	
//	Import me at the top of a class implementation, then write
//	SYNTHESIZE_SINGLETON_FOR_CLASS(MyClassName);
//	inside the @implementation MyClassName declaration.
//	
//	Also, add the line
//	+ (MyClassName *)sharedMyClassName;
//	to the header file for MyClassName so the singleton accessor method can be 
//	found from other source files if they #import the header.
//	
//	Once your class is a singleton, you can access the instance of it using the line
//	[MyClassName sharedMyClassName];
//	
//	http://cocoawithlove.com/2008/11/singletons-appdelegates-and-top-level.html
//

#define SYNTHESIZE_SINGLETON_FOR_CLASS(classname) \
 \
static classname *shared##classname = nil; \
 \
+ (classname *)shared##classname \
{ \
	@synchronized(self) \
	{ \
		if (shared##classname == nil) \
		{ \
			shared##classname = [[self alloc] init]; \
		} \
	} \
	 \
	return shared##classname; \
} \
 \
+ (id)allocWithZone:(NSZone *)zone \
{ \
	@synchronized(self) \
	{ \
		if (shared##classname == nil) \
		{ \
			shared##classname = [super allocWithZone:zone]; \
			return shared##classname; \
		} \
	} \
	 \
	return nil; \
} \
 \
- (id)copyWithZone:(NSZone *)zone \
{ \
	return self; \
} \
 \
- (id)retain \
{ \
	return self; \
} \
 \
- (NSUInteger)retainCount \
{ \
	return NSUIntegerMax; \
} \
 \
- (void)release \
{ \
} \
 \
- (id)autorelease \
{ \
	return self; \
}
